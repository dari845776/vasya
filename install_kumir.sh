#!/usr/bin/env bash
#
# install_kumir.sh — автоматическая установка КуМир (исполнитель «Черепаха»)
# для Astra Linux / Fly. Выполняет шаги 1–10 методички (установка, доустановка
# недостающей библиотеки, рабочий каталог, файл программы, запуск).
# НЕ выполняет удаление (шаги 11–14) — как и просили.
#
# Запуск:
#   bash install_kumir.sh
# (НЕ запускайте через sudo — скрипт сам спросит пароль там, где нужно.)

set -uo pipefail

# ---------- оформление вывода ----------
green() { printf '\033[1;32m%s\033[0m\n' "$1"; }
yellow(){ printf '\033[1;33m%s\033[0m\n' "$1"; }
red()   { printf '\033[1;31m%s\033[0m\n' "$1"; }
step()  { printf '\n\033[1;36m=== %s ===\033[0m\n' "$1"; }

# ---------- защита от запуска из-под root ----------
if [ "$(id -u)" -eq 0 ]; then
    red "Не запускайте скрипт через sudo/root."
    red "Запустите обычным пользователем:  bash install_kumir.sh"
    exit 1
fi

KUMIR_BIN="/usr/bin/kumir2-ide"
WORKDIR="$HOME/Kumir"

# ======================================================================
step "Шаг 1. Обновление списка пакетов"
# ======================================================================
sudo apt-get update

# ======================================================================
step "Шаг 2. Установка КуМира"
# ======================================================================
installed_ok=0

# --- Вариант А: установка из репозитория ---
yellow "Пробую установить из репозитория: sudo apt install kumir"
if sudo apt-get install -y kumir; then
    installed_ok=1
    green "КуМир установлен из репозитория."
else
    yellow "В репозитории пакета 'kumir' нет (или установка не удалась)."

    # --- Вариант Б (подстраховка): локальный .deb ---
    yellow "Ищу скачанный .deb в Загрузках и домашней папке..."
    deb_file=""
    for dir in "$HOME/Downloads" "$HOME/Загрузки" "$HOME" "$(pwd)"; do
        found="$(ls -1 "$dir"/kumir*amd64.deb "$dir"/kumir*.deb 2>/dev/null | head -n 1)"
        if [ -n "$found" ]; then deb_file="$found"; break; fi
    done

    if [ -n "$deb_file" ]; then
        green "Нашёл пакет: $deb_file"
        # apt сам подтянет зависимости (в т.ч. libqt4-svg)
        if sudo apt-get install -y "$deb_file"; then
            installed_ok=1
            green "КуМир установлен из локального .deb."
        fi
    else
        red "Скачанный .deb не найден."
    fi
fi

if [ "$installed_ok" -ne 1 ]; then
    red "Не удалось установить КуМир автоматически."
    red "Скачайте файл kumir_2.1.0_amd64.deb в папку Загрузки и запустите скрипт снова."
    exit 1
fi

# ======================================================================
step "Шаг 3. Доустановка недостающей библиотеки (libqt4-svg)"
# ======================================================================
# Это лечит ошибку: Can't load module CourseManager: Динамическая библиотека не найдена
if sudo apt-get install -y libqt4-svg; then
    green "Библиотека libqt4-svg на месте."
else
    yellow "libqt4-svg поставить не удалось — возможно, она уже встроена. Продолжаю."
fi

# ======================================================================
step "Шаг 4. Проверка установки"
# ======================================================================
if dpkg -l | grep -E '^ii' | grep -qi kumir; then
    green "Проверка пройдена — КуМир числится установленным:"
    dpkg -l | grep -E '^ii' | grep -i kumir
else
    yellow "В списке dpkg КуМир не виден явно, но продолжаю проверку файла запуска."
fi

if [ ! -x "$KUMIR_BIN" ]; then
    # вдруг бинарник лежит в другом месте
    alt="$(command -v kumir2-ide 2>/dev/null)"
    if [ -n "$alt" ]; then
        KUMIR_BIN="$alt"
    else
        red "Не найден исполняемый файл kumir2-ide. Установка, похоже, неполная."
        exit 1
    fi
fi
green "Файл запуска: $KUMIR_BIN"

# ======================================================================
step "Шаг 5. Рабочий каталог системы «Кумир»"
# ======================================================================
mkdir -p "$WORKDIR"
green "Создан рабочий каталог: $WORKDIR"
yellow "При первом запуске КуМир спросит рабочий каталог — просто нажмите «Да»."

# ======================================================================
step "Шаг 6. Готовый файл программы (Черепаха → прямоугольник)"
# ======================================================================
PRG="$WORKDIR/Прямоугольник.kum"
cat > "$PRG" << 'EOF'
использовать Черепаха
алг Прямоугольник
нач
  опустить хвост
  нц 2 раз
    вперед(200)
    вправо(90)
    вперед(100)
    вправо(90)
  кц
  поднять хвост
кон
EOF
green "Файл программы создан: $PRG"
yellow "В КуМире: Программа → Открыть → выберите этот файл, либо вставьте код вручную."

# ======================================================================
step "Шаг 7. Запуск КуМира"
# ======================================================================
yellow "Запускаю $KUMIR_BIN ..."
"$KUMIR_BIN" >/dev/null 2>&1 &
sleep 1
green "Готово! КуМир запущен."

echo
green "Что дальше (вручную, по методичке):"
echo "  • Выберите исполнитель «Черепаха»"
echo "  • Нажмите «Новая программа» (или откройте $PRG)"
echo "  • Сохраните код в своей папке и выложите его на сайт"
echo
yellow "Шаги удаления (11–14) намеренно НЕ выполнялись."
